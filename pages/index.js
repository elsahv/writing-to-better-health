export const getServerSideProps = async () => {
  const query = '*[_type in ["blogPosts", "cookingNotes", "]'
  const posts = await sanityClient.fetch(query)

  if(!posts.length) {
    return {
      props: {
        posts: [],
      },
    }
  } else {
    return {
      props: {
        posts,
      },
    }
  }
}

export default Home
