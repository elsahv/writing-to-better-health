export const getServerSideProps = async () => {
  const query = '*[ _type == "category"]'
  const categories = await sanityClient.fetch(query)

  if (!categories.length) {
    return {
      props: {
      categories: [],
      },
    }
  } else {
    return {
      props: {
      categories,
      },
    }
  }
}
 