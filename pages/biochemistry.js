const CookingNotes = ({ posts }) => {
  return (
    <>
      <Head>
        <title>Writing to Better Health | Cooking Notes</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>


      <Navbar />
      
      <BlogTitle>Biochemistry</BlogTitle>
                   <Wrapper> 
                   <PostsContainer>
                   {posts &&
                    posts.map((post, index) => (
                      <span key={index}>
                      <PostTitle>{post.biochemTitle}</PostTitle>
                      <ImageWrapper>
                      <img
                       src={urlFor(post.biochemImage)}
                       className="img"
                       width={350}
                       height={320}
                       />
                      </ImageWrapper>
                      </span>
                    ))
                    }
                    </PostsContainer>
                   </Wrapper>
    </>
  )
}
 
export const getServerSideProps = async () => {
  const query = '*[_type == "biochemistry"]'
  const posts = await sanityClient.fetch(query)

  if(!posts.length) {
    return {
      props: {
        posts: [],
      },
    }
  } else {
    return {
      props: {
        posts,
      },
    }
  }
}

export default CookingNotes
